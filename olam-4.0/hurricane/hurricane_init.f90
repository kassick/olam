integer, parameter :: ir_eyw = 7     ! radial index in axisymmetric profiles
integer, parameter :: ir_env = 22    ! radial index in axisymmetric profiles
real,    parameter :: rexpon_delc = 1.0

! TEMPERATURE PERTURBATION - assumed to be maximum at (r = 0, z = zmax_t)

real, parameter :: tmax = 16.
real, parameter :: z_tmax = 6500.
real, parameter :: zmax_t = 18000.
real, parameter :: rmax_t = 200.e3
real, parameter :: rscale_t = 40.e3  ! scaling radius for hyperbolic secant (64% of peak)
real, parameter :: zscale_t = 3000.  ! scaling delta_z for hyperbolic secant (64% of peak)

! TOTAL WATER SPECIFIC DENSITY PERTURBATION - assumed to be maximum at (r = 0, z = 0)

real, parameter :: smax = 4.e-3  ! (kg_water/kg_air)
real, parameter :: zmax_s = 5000.
real, parameter :: rmax_s = 200.e3

real :: tpert_rad, tpert_vert
real :: spert_rad, spert_vert
real :: vpert_rad, vpert_vert

real :: rad_eyw, rad_env
real :: circ_eyw, circ_env, circ, dcirc
real :: wrad1, wrad2

real :: del_circ(nr)

! lat/lon coords of eye center (correct observed location)

! Frances location on 1 Sept 2004 at 00 UTC

hcentlat = 20.6
hcentlon = -66.3

! Frances location on 2 Sept 2004 at 00 UTC

! hcentlat = 22.3
! hcentlon = -71.4

! Find "earth" coordinates of hurricane center

zeh = erad * sin(hcentlat * pio180)
reh = erad * cos(hcentlat * pio180)  ! distance from earth axis
xeh = reh  * cos(hcentlon * pio180)
yeh = reh  * sin(hcentlon * pio180)

! Components of unit vector outward normal to earth surface at hurricane center

wnxh = xeh / erad
wnyh = yeh / erad
wnzh = zeh / erad

if (cfield == 'U' .or. cfield == 'V') then

! Compute radial profile of circulation perturbation

   rad_eyw = radius_pert(ir_eyw)
   rad_env = radius_pert(ir_env)

   circ_eyw = circ_avg(ir_eyw) + pi2 * rad_eyw * delv_eyw
   circ_env = circ_avg(ir_env)

   del_circ(:) = 0.

   print*, 'hi1 ',rad_eyw,rad_env,circ_eyw,circ_env

   do ir = ir_eyw,ir_env

      circ = circ_eyw + (circ_env - circ_eyw) &
           * ((radius_pert(ir) - rad_eyw) / (rad_env - rad_eyw)) ** rexpon_delc 
      
      del_circ(ir) = max(0., circ - circ_avg(ir))
      
   print*, 'hi2 ',ir,circ,circ_avg(ir),del_circ(ir)

   enddo   

endif

! If cfield = 'T', enhance thermodynamic fields

if (cfield == 'T') then

   call psub()
!----------------------------------------------------------------------
   do j = 1,jtab_w(7)%jend(1); iw = jtab_w(7)%iw(j)  ! jend(1) = hardwired for mrl 1
!----------------------------------------------------------------------
   call qsub('W',iw)

! Distance of this IW point from eye center

      rad = sqrt((xew(iw)-xeh)**2 + (yew(iw)-yeh)**2 + (zew(iw)-zeh)**2)
   
! Skip hurricane assimilation for all points outside limiting perturbation radius

      if (rad >= max(rmax_t,rmax_s)) go to 100

! Radial factor of temperature perturbation

      tpert_rad = 2. / (exp(rad/rscale_t) + exp(-rad/rscale_t))

! Radial dependence of total water perturbation at height of maximum

      spert_rad = smax * (1. - (rad / rmax_s))

! Vertical loop over T levels

      do k = lpw(iw),mza-1

! Skip hurricane assimilation for all points above limiting height

         if (zt(k) >= max(zmax_t,zmax_s)) exit

! Vertical factor of temperature perturbation

         zdif = abs(zt(k) - z_tmax)

         tpert_vert = 2. / (exp(zdif/zscale_t) + exp(-zdif/zscale_t))

! Vertical reduction of total water perturbation at heights off maximum

         spert_vert = -smax * (zt(k) / zmax_s)

! Add vertical and radial parts of perturbations

         temp_pert0 = tmax * tpert_rad * tpert_vert
          shw_pert0 = max(0.,spert_rad + spert_vert)

! Compute theta perturbation from temperature perturbation

         exner = (press(k,iw) / p00) ** rocp   ! Defined WITHOUT CP factor
         deltheta = temp_pert0 / exner

! Add theta and shw perturbations to model fields

         thil(k,iw)  = thil(k,iw)  + deltheta
         theta(k,iw) = theta(k,iw) + deltheta
      
         sh_w(k,iw) = sh_w(k,iw) + shw_pert0
         sh_v(k,iw) = sh_w(k,iw)

      enddo

      100 continue

!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
! Hydrostatically balance modified initial profile
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!

! Select model level as top boundary condition for hydrostatic integration:
! Profile remains unchanged at and above this level 

      kbc = 35

! Carry out iterative hydrostatic balance procedure

      do iter = 1,100

         do k = kbc-1,1,-1

!  Compute density - (ASSUME MICPHYS LEVEL = 1 FOR NOW)

            rho(k,iw) = press(k,iw) ** cvocp * p00k  &
               / (theta(k,iw) * (rdry * (1. - sh_v(k,iw)) + rvap * sh_v(k,iw)))

! Hydrostatically integrate downward using weighting to damp oscillations

            pkhyd = press(k+1,iw)  &
               + gravo2 * (rho(k+1,iw) * dzt(k+1) + rho(k,iw) * dzt(k))
            press(k,iw) = .05 * press(k,iw) + .95 * pkhyd

         enddo
      enddo

   enddo
   call rsub('W_frances_a',7)

! If cfield = 'U', initialize UC field

elseif (cfield == 'U') then

   call psub()
!----------------------------------------------------------------------
   do j = 1,jtab_u(7)%jend(1); iu = jtab_u(7)%iu(j)  ! jend(1) = hardwired for mrl 1
      iw1 = itab_u(iu)%iw(1); iw2 = itab_u(iu)%iw(2)
!----------------------------------------------------------------------
   call qsub('U',iu)

! Distance of this point from eye center

      rad = sqrt((xeu(iu)-xeh)**2 + (yeu(iu)-yeh)**2 + (zeu(iu)-zeh)**2)

! Skip hurricane assimilation for all points outside specified radius

      if (rad >= rad_env) cycle

! Determine interpolation point in radial dimension

      ir = 1
      do while (rad > radius_pert(ir+1))
         ir = ir + 1
      enddo

      wrad2 = (rad - radius_pert(ir)) / (radius_pert(ir+1) - radius_pert(ir))
      wrad1 = 1. - wrad2
   
! Interpolate circulation perturbation table values to rad

      dcirc = wrad1 * del_circ(ir  ) &
            + wrad2 * del_circ(ir+1)

! Evaluate perturbation tangential velocity

      if (rad <= rad_eyw) then
         vpert_rad = delv_eyw * rad / rad_eyw
      else
         vpert_rad = dcirc / (pi2 * rad)
      endif

! Unit normal vector components from hurricane center to current IU point

      unxrad = (xeu(iu) - xeh) / rad
      unyrad = (yeu(iu) - yeh) / rad
      unzrad = (zeu(iu) - zeh) / rad

! Unit vector components in direction of tangential vortex wind

      unxtan = wnyh * unzrad - wnzh * unyrad
      unytan = wnzh * unxrad - wnxh * unzrad
      unztan = wnxh * unyrad - wnyh * unxrad

! Vertical loop over T levels

      do k = lpu(iu),mza-1

! Skip hurricane assimilation for all points above limiting height

         if (zt(k) >= zmax_delv) exit

! Vertical dependence of tangential velocity perturbation

         vpert_vert = 1. - (zt(k) / zmax_delv) ** zexpon_delv

! Combine vertical and radial parts of perturbation

         vtan_pert0 = vpert_rad * vpert_vert

! Add enhanced vortex to winds interpolated from NCEP reanalysis

         uc(k,iu) = uc(k,iu) + vtan_pert0  &
            * (unx(iu) * unxtan + uny(iu) * unytan + unz(iu) * unztan)

         umc(k,iu) = uc(k,iu) * .5 * (rho(k,iw1) + rho(k,iw2))
         ump(k,iu) = umc(k,iu)

      enddo

   enddo
   call rsub('U_frances_a',7)

! If cfield = 'V', initialize VC field

elseif (cfield == 'V') then

   call psub()
!----------------------------------------------------------------------
   do j = 1,jtab_v(7)%jend(1); iv = jtab_v(7)%iv(j)  ! jend(1) = hardwired for mrl 1
      iw1 = itab_v(iv)%iw(1); iw2 = itab_v(iv)%iw(2)
!----------------------------------------------------------------------
   call qsub('V',iv)
 
! Distance of this point from eye center

      rad = sqrt((xev(iv)-xeh)**2 + (yev(iv)-yeh)**2 + (zev(iv)-zeh)**2)

! Skip hurricane assimilation for all points outside specified radius

      if (rad >= rad_env) cycle

! Determine interpolation point in radial dimension

      ir = 1
      do while (rad > radius_pert(ir+1))
         ir = ir + 1
      enddo

      wrad2 = (rad - radius_pert(ir)) / (radius_pert(ir+1) - radius_pert(ir))
      wrad1 = 1. - wrad2
   
! Interpolate circulation perturbation table values to rad

      dcirc = wrad1 * del_circ(ir  ) &
            + wrad2 * del_circ(ir+1)

! Evaluate perturbation tangential velocity

      if (rad <= rad_eyw) then
         vpert_rad = delv_eyw * rad / rad_eyw
      else
         vpert_rad = dcirc / (pi2 * rad)
      endif

! Unit normal vector components from hurricane center to current IV point

      vnxrad = (xev(iv) - xeh) / rad
      vnyrad = (yev(iv) - yeh) / rad
      vnzrad = (zev(iv) - zeh) / rad

! Unit vector components in direction of tangential vortex wind

      vnxtan = wnyh * vnzrad - wnzh * vnyrad
      vnytan = wnzh * vnxrad - wnxh * vnzrad
      vnztan = wnxh * vnyrad - wnyh * vnxrad

! Vertical loop over T levels

      do k = lpv(iv),mza-1

! Skip hurricane assimilation for all points above limiting height

         if (zt(k) >= zmax_delv) exit

! Vertical dependence of tangential velocity perturbation

         vpert_vert = 1. - (zt(k) / zmax_delv) ** zexpon_delv

! Combine vertical and radial parts of perturbation

         vtan_pert0 = vpert_rad * vpert_vert

! Add enhanced vortex to winds interpolated from NCEP reanalysis

         vc(k,iv) = vc(k,iv) + vtan_pert0  &
            * (vnx(iv) * vnxtan + vny(iv) * vnytan + vnz(iv) * vnztan)

         vmc(k,iv) = vc(k,iv) * .5 * (rho(k,iw1) + rho(k,iw2))
         vmp(k,iv) = vmc(k,iv)
      enddo

   enddo
   call rsub('V_frances_a',7)

endif

return
end subroutine hurricane_init1

