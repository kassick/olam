subroutine hurricane_track()

use misc_coms, only: meshtype

implicit none

integer :: init_hurricane

integer, save :: newcall=0

real, save :: hlat,hlon

! Call subroutine hurricane_init to check current value of init_hurricane; 
! if it is not = 1, return

call hurricane_init('T',init_hurricane)

if (init_hurricane /= 1) return

! If this is first diagnosis of vortex center location, assign initial location

if (newcall /= 1) then
   newcall = 1

! Hurricane Frances location on 1 Sept 2004 at 00 UTC

   hlat = 20.6
   hlon = -66.3
endif

! Get updated hurricane center location

call vortex_center_diagnose(hlat,hlon)

call vortex_diagnose('T',2,hlat,hlon)

if (meshtype == 1) then
   call vortex_diagnose('U',2,hlat,hlon)
else
   call vortex_diagnose('V',2,hlat,hlon)
endif

end subroutine hurricane_track

