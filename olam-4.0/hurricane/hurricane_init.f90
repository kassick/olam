subroutine hurricane_init(cfield,init_hurricane)

implicit none

character(1), intent(in) :: cfield
integer, intent(inout) :: init_hurricane

real :: hlat,hlon

!----------------------------------------------------------------
! THE USER MUST SET INIT_HURRICANE HERE TO PERFORM THE FOLLOWING:
!
! 0 = no hurricane initialization
! 1 = initialize for first dynamic initialization cycle
! 2 = initialize for second or subsequent initialization cycles
!     or for actual simulation
!----------------------------------------------------------------

init_hurricane = 2

if (init_hurricane == 0) then

   return

elseif (init_hurricane == 1) then

! Hurricane Frances location on 1 Sept 2004 at 00 UTC

   hlat = 20.6
   hlon = -66.3

   call vortex_diagnose(cfield,1,hlat,hlon)
   call hurricane_init1(cfield)       ! FIRST-GUESS VORTEX PERT

elseif (init_hurricane == 2) then

   call hurricane_init2(cfield)       ! RE-INITIALIZE WITH NEW PERT

endif

end subroutine hurricane_init

