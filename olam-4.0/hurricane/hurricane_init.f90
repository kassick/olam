subroutine vortex_diagnose0(cfield,hlat,hlon, &
                           thil_model,theta_model,shw_model,shv_model,vtan_model)

use mem_ijtabs
use mem_basic
use misc_coms
use mem_grid
use consts_coms
use hcane_rz

implicit none

character(1), intent(in) :: cfield

real, intent(in) :: hlat,hlon

real, intent(out) ::  thil_model(mza,nr) ! temperature (K)
real, intent(out) :: theta_model(mza,nr) ! temperature (K)
real, intent(out) ::   shw_model(mza,nr) ! total water specific density (kg/kg)
real, intent(out) ::   shv_model(mza,nr) ! vapor specific density (kg/kg)
real, intent(out) ::  vtan_model(mza,nr) ! tangential wind (m/s)

integer :: iw,i,j,k,ngr,iu,iv,iw1,iw2
real :: zeh,reh,xeh,yeh
real :: wnxh,wnyh,wnzh
real :: unxrad,unyrad,unzrad,unxtan,unytan,unztan
real :: vnxrad,vnyrad,vnzrad,vnxtan,vnytan,vnztan

real :: rad,rrad,wrad1,wrad2,thil_mod,theta_mod,sh_w_mod,sh_v_mod,vtan_mod
integer :: irad,ir

real :: weight_t(nr)   ! weight array for T points
real :: weight_u(nr)   ! weight array for U points
real :: weight_v(nr)   ! weight array for V points

! Find "earth" coordinates of hurricane center

zeh = erad * sin(hlat * pio180)
reh = erad * cos(hlat * pio180)  ! distance from earth axis
xeh = reh  * cos(hlon * pio180)
yeh = reh  * sin(hlon * pio180)

! Components of unit vector outward normal to earth surface at hurricane center

wnxh = xeh / erad
wnyh = yeh / erad
wnzh = zeh / erad

! If cfield = 'T', diagnose thermodynamic fields

if (cfield == 'T') then

    thil_model(:,:) = 0.
   theta_model(:,:) = 0.
     shw_model(:,:) = 0.
     shv_model(:,:) = 0.

   weight_t(:) = 0.

   call psub()
!----------------------------------------------------------------------
   do j = 1,jtab_w(7)%jend(1); iw = jtab_w(7)%iw(j)  ! jend(1) = hardwired for mrl 1
!----------------------------------------------------------------------
   call qsub('W',iw)

! Distance of this IW point from eye center

      rad = sqrt((xew(iw)-xeh)**2 + (yew(iw)-yeh)**2 + (zew(iw)-zeh)**2)
   
! Skip hurricane assimilation for all points outside specified radius

      if (rad >= radius_pert(nr) - 1.) cycle

! Determine interpolation point in radial dimension

      irad = 1
      do while (rad > radius_pert(irad+1))
         irad = irad + 1
      enddo

      wrad2 = (rad - radius_pert(irad)) / (radius_pert(irad+1) - radius_pert(irad))
      wrad1 = 1. - wrad2
   
      weight_t(irad)   = weight_t(irad)   + wrad1
      weight_t(irad+1) = weight_t(irad+1) + wrad2

! Vertical loop over T levels

      do k = lpw(iw),mza-1

! Diagnose axisymmetric component of model's own vortex

         thil_model(k,irad)   = thil_model(k,irad)   + wrad1 * thil(k,iw)
         thil_model(k,irad+1) = thil_model(k,irad+1) + wrad2 * thil(k,iw)

         theta_model(k,irad)   = theta_model(k,irad)   + wrad1 * theta(k,iw)
         theta_model(k,irad+1) = theta_model(k,irad+1) + wrad2 * theta(k,iw)

         shw_model(k,irad)   = shw_model(k,irad)   + wrad1 * sh_w(k,iw)
         shw_model(k,irad+1) = shw_model(k,irad+1) + wrad2 * sh_w(k,iw)

         shv_model(k,irad)   = shv_model(k,irad)   + wrad1 * sh_v(k,iw)
         shv_model(k,irad+1) = shv_model(k,irad+1) + wrad2 * sh_v(k,iw)

      enddo

   enddo

! Convert sums to averages

   do irad = 1,nr

      if (weight_t(irad) < 1.e-6) cycle

      do k = 2,mza-1

         thil_model (k,irad) = thil_model (k,irad) / weight_t(irad)
         theta_model(k,irad) = theta_model(k,irad) / weight_t(irad)
         shw_model  (k,irad) = shw_model  (k,irad) / weight_t(irad)
         shv_model  (k,irad) = shv_model  (k,irad) / weight_t(irad)

      enddo

   enddo

! If cfield = 'U', diagnose UC field

elseif (cfield == 'U') then

   vtan_model(:,:) = 0.

   weight_u(:) = 0.

!----------------------------------------------------------------------
   do j = 1,jtab_u(7)%jend(1); iu = jtab_u(7)%iu(j)  ! jend(1) = hardwired for mrl 1
      iw1 = itab_u(iu)%iw(1); iw2 = itab_u(iu)%iw(2)
!----------------------------------------------------------------------

! Distance of this point from eye center

      rad = sqrt((xeu(iu)-xeh)**2 + (yeu(iu)-yeh)**2 + (zeu(iu)-zeh)**2)

! Skip hurricane assimilation for all points outside specified radius

      if (rad >= radius_pert(nr) - 1.) cycle

! Determine interpolation point in radial dimension

      irad = 1
      do while (rad > radius_pert(irad+1))
         irad = irad + 1
      enddo

      wrad2 = (rad - radius_pert(irad)) / (radius_pert(irad+1) - radius_pert(irad))
      wrad1 = 1. - wrad2
   
! Unit normal vector components from hurricane center to current IU point

      unxrad = (xeu(iu) - xeh) / rad
      unyrad = (yeu(iu) - yeh) / rad
      unzrad = (zeu(iu) - zeh) / rad

! Unit vector components in direction of tangential vortex wind

      unxtan = wnyh * unzrad - wnzh * unyrad
      unytan = wnzh * unxrad - wnxh * unzrad
      unztan = wnxh * unyrad - wnyh * unxrad

      weight_u(irad)   = weight_u(irad)   + wrad1
      weight_u(irad+1) = weight_u(irad+1) + wrad2

! Vertical loop over T levels

      do k = lpu(iu),mza-1

! Diagnose axisymmetric component of model's own vortex

         vtan_mod = uc(k,iu) &
                  * (unx(iu) * unxtan + uny(iu) * unytan + unz(iu) * unztan) &
                  + vc(k,iu) &
                  * (vnx(iu) * unxtan + vny(iu) * unytan + vnz(iu) * unztan)

         vtan_model(k,irad)   = vtan_model(k,irad)   + wrad1 * vtan_mod
         vtan_model(k,irad+1) = vtan_model(k,irad+1) + wrad2 * vtan_mod
       
      enddo

   enddo

! Convert sums to averages

   do irad = 1,nr

      if (weight_u(irad) < 1.e-6) cycle

      do k = 2,mza-1

         vtan_model(k,irad) = vtan_model(k,irad) / weight_u(irad)

      enddo

   enddo

! If cfield = 'V', diagnose VC field

elseif (cfield == 'V') then

   vtan_model(:,:) = 0.

   weight_v(:) = 0.

!----------------------------------------------------------------------
   do j = 1,jtab_v(7)%jend(1); iv = jtab_v(7)%iv(j)  ! jend(1) = hardwired for mrl 1
      iw1 = itab_v(iv)%iw(1); iw2 = itab_v(iv)%iw(2)
!----------------------------------------------------------------------
 
! Distance of this point from eye center

      rad = sqrt((xev(iv)-xeh)**2 + (yev(iv)-yeh)**2 + (zev(iv)-zeh)**2)

! Skip hurricane assimilation for all points outside specified radius

      if (rad >= radius_pert(nr) - 1.) cycle

! Determine interpolation point in radial dimension

      irad = 1
      do while (rad > radius_pert(irad+1))
         irad = irad + 1
      enddo

      wrad2 = (rad - radius_pert(irad)) / (radius_pert(irad+1) - radius_pert(irad))
      wrad1 = 1. - wrad2
   
! Unit normal vector components from hurricane center to current IV point

      vnxrad = (xev(iv) - xeh) / rad
      vnyrad = (yev(iv) - yeh) / rad
      vnzrad = (zev(iv) - zeh) / rad

! Unit vector components in direction of tangential vortex wind

      vnxtan = wnyh * vnzrad - wnzh * vnyrad
      vnytan = wnzh * vnxrad - wnxh * vnzrad
      vnztan = wnxh * vnyrad - wnyh * vnxrad

      weight_v(irad)   = weight_v(irad)   + wrad1
      weight_v(irad+1) = weight_v(irad+1) + wrad2

! Vertical loop over T levels

      do k = lpv(iv),mza-1

! Diagnose axisymmetric component of model's own vortex

         vtan_mod = vc(k,iv) &
                  * (vnx(iv) * vnxtan + vny(iv) * vnytan + vnz(iv) * vnztan) &
                  + uc(k,iv) &
                  * (unx(iv) * vnxtan + uny(iv) * vnytan + unz(iv) * vnztan)

         vtan_model(k,irad)   = vtan_model(k,irad)   + wrad1 * vtan_mod
         vtan_model(k,irad+1) = vtan_model(k,irad+1) + wrad2 * vtan_mod

      enddo

   enddo

! Convert sums to averages

   do irad = 1,nr

      if (weight_v(irad) < 1.e-6) cycle

      do k = 2,mza-1

         vtan_model(k,irad) = vtan_model(k,irad) / weight_v(irad)

      enddo

   enddo

endif

return
end subroutine vortex_diagnose0


